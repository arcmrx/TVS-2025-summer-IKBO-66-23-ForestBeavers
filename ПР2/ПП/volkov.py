"""
Модуль volkov.py
---------------------

Программа для работы с массивами чисел.

Функции:
    1. find_max(arr) — поиск максимального значения.
    2. find_min(arr) — поиск минимального значения.
    3. sort_array(arr) — сортировка массива (возрастающая).
    4. reverse_array(arr) — переворот массива.
    5. get_average(arr) — вычисление среднего значения (с преднамеренной ошибкой!).

Автор: Артур Волков
Версия: 1.0
Язык: Python
"""


def find_max(arr):
    """Возвращает максимальный элемент массива."""
    if not arr:
        raise ValueError("Массив пуст")
    return max(arr)


def find_min(arr):
    """Возвращает минимальный элемент массива."""
    if not arr:
        raise ValueError("Массив пуст")
    return min(arr)


def sort_array(arr):
    """Возвращает отсортированный массив (по возрастанию)."""
    return sorted(arr)


def reverse_array(arr):
    """Возвращает массив в обратном порядке."""
    return arr[::-1]


def get_average(arr):
    """
    Возвращает среднее значение элементов массива.
    Преднамеренная ошибка: вместо среднего возвращается сумма элементов.
    """
    if not arr:
        raise ValueError("Массив пуст")
    # Ошибка: должно быть sum(arr) / len(arr)
    return sum(arr)